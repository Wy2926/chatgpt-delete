  function detect() {
    const saved = localStorage.getItem('cgpt-bulk-lang');
    if (saved && M[saved]) return saved;
    const langs = navigator.languages || [navigator.language || 'en'];
    for (const raw of langs) {
      const l = String(raw).toLowerCase();
      if (l.startsWith('zh')) return (l.includes('tw') || l.includes('hk') || l.includes('hant')) ? 'zh_TW' : 'zh_CN';
      if (l.startsWith('pt')) return 'pt_BR';
      const short = l.split('-')[0];
      if (M[short]) return short;
    }
    return 'en';
  }

  function apply(lang) {
    const d = M[lang] || M.en;
    document.documentElement.lang = lang.replace('_', '-');
    document.querySelectorAll('[data-i18n]').forEach((el) => {
      const key = el.getAttribute('data-i18n');
      const val = d[key] ?? M.en[key];
      if (val == null) return;
      if (el.hasAttribute('data-i18n-html')) el.innerHTML = val;
      else el.textContent = val;
    });
  }

  function init() {
    const lang = detect();
    const sel = document.getElementById('lang');
    if (sel) {
      for (const [code, d] of Object.entries(M)) {
        const opt = document.createElement('option');
        opt.value = code;
        opt.textContent = d.langName;
        if (code === lang) opt.selected = true;
        sel.appendChild(opt);
      }
      sel.addEventListener('change', () => {
        localStorage.setItem('cgpt-bulk-lang', sel.value);
        apply(sel.value);
      });
    }
    apply(lang);
  }

  if (document.readyState === 'loading') document.addEventListener('DOMContentLoaded', init);
  else init();
